package atividade04;

public class Veiculo {

    int id;
    String modelo;
    double valor;

    // Construtor vazio
    public Veiculo() {
    }

    // Construtor cheio
    public Veiculo(int id, String modelo, double valor) {
        this.id = id;
        this.modelo = modelo;
        this.valor = valor;
    }
}
